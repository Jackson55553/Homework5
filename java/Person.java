class Person {
    String name;
    String position;
    String email;
    int phoneNumber;
    int wage;
    int age;

Person(String name, String position, String email, int phoneNumber, int wage, int age){
    this.name = name;
    this.position = position;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.wage = wage;
    this.age = age;
}
}